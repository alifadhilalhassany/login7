<?php
// اسم الملف: dash.php

// ابدأ الجلسة
session_start();

// تحقق مما إذا كانت متغير الجلسة موجودًا، إذا لم يكن، قم بتعيينه للقيمة الافتراضية 0
if (!isset($_SESSION['user_visits'])) {
    $_SESSION['user_visits'] = 0;
}

// زيادة عدد الزيارات بمقدار واحد عند كل زيارة لهذه الصفحة
$_SESSION['user_visits']++;

// عرض عدد الزيارات للمستخدم
echo "عدد الزيارات الخاصة بك: " . $_SESSION['user_visits'];
?>
